    private void btnFinishTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishTaskActionPerformed
        if(tbldailytask.getSelectedRow()>-1){
            if(((String) tbldailytask.getValueAt(tbldailytask.getSelectedRow(), 1)).equals("-")){
                int tmp=JOptionPane.showConfirmDialog(this,"Do you want delete this ?","Delete ?",JOptionPane.YES_NO_CANCEL_OPTION);
                if (tmp==0){
                    cntrl.finishDailyTask((String) tbldailytask.getValueAt(tbldailytask.getSelectedRow(), 0));
                    cntrl.fetchDailyTask(tbldailytask);
                }
            }else{
                JOptionPane.showMessageDialog(this,"You can't delete this work form here\n you can delete this from 'Designworks' page","Select!!",1);
            }
        }
        else
            JOptionPane.showMessageDialog(this,"Select row from the table","Select!!",1);
    }//GEN-LAST:event_btnFinishTaskActionPerformed

